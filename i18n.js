// Shared Hindi/English i18n engine for NEET-AI. Include on every page:
// <script src="./i18n.js"></script>
// Then tag elements with data-i18n="namespace.key" (textContent),
// data-i18n-placeholder="namespace.key" (input placeholder),
// or data-i18n-title="namespace.key" (title attribute).

const LANG_KEY = 'neet_lang';

function getLang() {
  return localStorage.getItem(LANG_KEY) || 'en';
}

function setLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
  applyTranslations();
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

// Devanagari digit conversion, so numbers read in Hindi when Hindi is active.
const HINDI_DIGITS = ['०', '१', '२', '३', '४', '५', '६', '७', '८', '९'];

function toHindiDigits(value) {
  return String(value).replace(/[0-9]/g, d => HINDI_DIGITS[d]);
}

function localizeNum(value) {
  return getLang() === 'hi' ? toHindiDigits(value) : String(value);
}

// Dictionary entries with embedded scientific notation (C3, C4, 3-PGA) where
// Devanagari digits would break the notation — kept in Latin numerals.
const NUMERAL_EXEMPT_KEYS = new Set([
  'index.demoUser1', 'index.demoAi1', 'index.demoUser2', 'index.demoAi2'
]);

function t(namespace, key) {
  const ns = TRANSLATIONS[namespace];
  if (!ns) return key;
  const lang = getLang();
  const value = (ns[lang] && ns[lang][key]) || (ns.en && ns.en[key]) || key;
  if (lang === 'hi' && !NUMERAL_EXEMPT_KEYS.has(namespace + '.' + key)) {
    return toHindiDigits(value);
  }
  return value;
}

function applyTranslations() {
  const lang = getLang();
  document.documentElement.lang = lang === 'hi' ? 'hi' : 'en';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const [ns, key] = el.getAttribute('data-i18n').split('.');
    el.textContent = t(ns, key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const [ns, key] = el.getAttribute('data-i18n-placeholder').split('.');
    el.placeholder = t(ns, key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const [ns, key] = el.getAttribute('data-i18n-title').split('.');
    el.title = t(ns, key);
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const [ns, key] = el.getAttribute('data-i18n-html').split('.');
    el.innerHTML = t(ns, key);
  });
  // Static numbers hardcoded in HTML (not in the dictionary) — e.g. stat counters, prices.
  document.querySelectorAll('[data-i18n-num]').forEach(el => {
    if (el.dataset.i18nNumSrc === undefined) el.dataset.i18nNumSrc = el.textContent;
    el.textContent = localizeNum(el.dataset.i18nNumSrc);
  });

  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.textContent = lang === 'en' ? 'हिं' : 'EN';
    btn.setAttribute('aria-label', lang === 'en' ? 'Switch to Hindi' : 'Switch to English');
  });
}

function toggleLang() {
  setLang(getLang() === 'en' ? 'hi' : 'en');
}

